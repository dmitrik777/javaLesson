package main.java.javaLesson.primitives;

public class G_Escape {
    public static void main(String[] args) {
        System.out.println("hello 'Dima'");
        System.out.println("hello \"Dima\"");
        System.out.println("hello \r Dima");
        System.out.println("hello \\\\ dima");
    }
}
